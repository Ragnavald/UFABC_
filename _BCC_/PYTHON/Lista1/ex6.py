n1 = int(input("Digite o primeiro número: "))
n2 = int(input("Digite o segundo número: "))
print(str(n1)+" + "+str(n2)+" = "+str(n1+n2)) #str() método para a impressão em uma string
print(str(n1)+" - "+str(n2)+" = "+str(n1-n2))
print(str(n1)+" * "+str(n2)+" = "+str(n1*n2))
print(str(n1)+" / "+str(n2)+" = "+str(n1/n2))